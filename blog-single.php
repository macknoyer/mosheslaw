						<h2 class="block-title__title">Types of Construction Accident Compensation</h2>
						<p class="block-title__text-two"></p>
						<p>Just as there are many types of construction accident injuries, there are many ways that you could be entitled to recover compensation after a construction accident in NYC. The type of claim that you have matters because it will determine both who you can sue and how you should prove your case.</p>
						<h3>Worker’s Compensation Benefits Claim</h3>
						<p>Any employee injured at a New York construction site while performing any portion of their daily work tasks is eligible to file a claim under the worker’s compensation system. In the worker’s compensation system, benefits are paid by your employer’s worker’s compensation insurer. These benefits include full coverage of medical costs plus a partial compensation for lost wages. The construction accident lawyers at the Law Office of Yuriy Moshes can help you better understand the compensation that you will be entitled to under the worker’s compensation system. All workers are eligible for worker’s compensation regardless of whether either you or your employer was at-fault or negligent.</p>
						<p>Worker’s compensation claimants should note that filing for worker’s compensation results in forfeiting all rights to appear in court on the same claim. In fact, if an injured employee has a valid worker’s compensation claim, that employee must file for worker’s compensation rather than file in normal court. For this reason, it is important to hire a specialized New York City construction accident lawyer to help file your claim.</p>
						<h3>Scaffolding Injury and Falling Injury Claims</h3>
						<p>Under New York’s unique construction labor laws, special compensation rights exist for workers who are injured while on scaffolds or who fall from high places. Under New York Labor Law Section 240, employers and construction contractors must take special precautions when erecting and managing scaffolding. When an injury occurs, your employer may be separately liable under this law. If you have been injured on scaffolding, you should consult a construction accident attorney about your legal options.</p>
						<h3>Barriers and Signage Regulations Violations Injury Claims</h3>
						<p>Similarly to the scaffolding claims law, the New York Labor Law Section 241 requires construction sites to meet additional safety requirements that help alert construction workers to on-site hazards. These requirements include the placement of barriers around construction elevator, PPE provisions, and slipping, falling, and tripping protections. For additional information, consult with a New York construction accident lawyer.</p>
						<h3>Third-Party Accident Claims</h3>
						<p>As explained above, worker’s compensation claimants may be barred from pursuing a legal claim against their employer. However, if a third party such as another construction subcontractor, a supplier, or an equipment manufacturer was responsible for your injury, you should also consider filing a third-party accident claim.</p>
						<h3>Bystander Accident Claim</h3>
						<p>If you or a loved one becomes a victim of a construction accident in NYC but were not employed, you may still have a claim for legal compensation. Passerby, bystanders, and construction site visitors may file a personal injury lawsuit against the at-fault party for any injuries suffered at the construction site.</p>
						<h3>Filing for Disability</h3>
						<p>In addition to any of the above claims, you may also be eligible to file for social security disability. Many severe construction site accidents can result in permanent injury, which may entitle you to receive disability coverage from the Social Security Administration. The attorneys at the Law Office of Yuriy Moshes can help walk you through the disability filing procedure.</p>
